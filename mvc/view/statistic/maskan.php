    function upsum() {
        $.ajax('/MonthStatisticsByMVC/maskan/getAllmskn/', {
            type: 'post',
            dataType: "json",
            data: {
                year: <?= $data['Year']; ?>
            },
            success: function(data) {
                const dValues = Object.values(data[0]);
                let sum = 0;
                for (let i = 0; i < dValues.length - 5; i++) {
                    sum += +dValues[i];
                }
                let s = String(<?= json_encode($data['Month']); ?>);
                gfield = 8;
                $.ajax('/MonthStatisticsByMVC/maskan/updateMskn/' + gfield, {
                    type: 'post',
                    dataType: "json",
                    data: {
                        'goalf': sum,
                        'yr': <?= $data['Year']; ?>,
                        'mn': s,
                    },
                    success: function(data) {
                        if (data['disableEdit'] == true) {
                            alert('عملیات ویرایش غیر فعال می باشد لطفا به مدیر سیستم مراجعه کنید.');
